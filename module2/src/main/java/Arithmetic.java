public class Arithmetic {

    static double add(final double number1, final double number2) {
        double sum;
        sum = number1 + number2;
        return sum;
    }

    static double sub(final double number1, final double number2) {
        double sub;
        sub = number1 - number2;
        return sub;
    }

    static double mul(final double number1, final double number2) {
        double mul;
        mul = number1 * number2;
        return mul;
    }

    static double div(final double number1, final double number2) {
        double div;
        div = number1 / number2;
        return div;

    }
}
